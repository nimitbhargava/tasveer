    public function uploadTasveer(Request $request)
    {
        $input = $request->all();

        if($file = $request->file('path')) {
            $name = $file->getClientOriginalName();
            $file->move('tasveer/' . $input['user_id'], $name);
            $input['path'] = $name;
        }

        Photo::create($input);
        return redirect()->route('user.upload');
    }
